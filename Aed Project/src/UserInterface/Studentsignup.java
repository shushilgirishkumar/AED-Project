     private void addtheperson()
    {
        String Name = txt.getText();
        String Department = txtdepartment.getText();
        String Campus = txtcampus.getText();
        String email = txtemail.getText();
        int Age = Integer.parseInt(txtage.getText());
        int  phoneno= Integer.parseInt(txtphonenumber.getText());
        String Acceptedyear = txtacceptedyear.getText();
        String Gradyear = txtgraduatedyear.getText();
        String Password = txtpassword.getText();
        String NEUID = txtneu.getText();
        Student.Studentde psc = history.addnuser();

        
        
        psc.setName(Name);
        psc.setCampus(Campus);
        psc.setNEUID(NEUID);
        psc.setAge(Age);
        psc.setGradyear(Gradyear);
        psc.setPhoneno(phoneno);
        psc.setEmail_Id(email);
        psc.setAcceptedyear(Acceptedyear);
        psc.setPassword(Password);
        psc.setSpecalization(Department);
        
        
        
        txt.setText("");
        txtdepartment.setText("");
        txtcampus.setText("");
        txtemail.setText("");
        txtage.setText("");
        txtphonenumber.setText("");
        txtacceptedyear.setText("");
        txtgraduatedyear.setText("");
        txtpassword.setText("");
        txtneu.setText("");
      
        
        
        
    }
    
    
    
