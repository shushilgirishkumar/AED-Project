    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
  boolean duplicatePerson = false;

    for (Student.Studentde psc : history.getHistory()) {
        if (psc.getNEUID().equals(txtneu.getText())) {
            duplicatePerson = true;
            break;
        }
    }

    if (!duplicatePerson) {
        addtheperson();
        JOptionPane.showMessageDialog(this, "New Person is added");
    } else {
        JOptionPane.showMessageDialog(this, "NEUID has been used by another Student. Enter a new one.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

