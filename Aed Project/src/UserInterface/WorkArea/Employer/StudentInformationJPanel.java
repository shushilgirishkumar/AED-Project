         if(Role.equals("")){
            JOptionPane.showMessageDialog(null, "invalid input", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
         if(Status.equals("")){
            JOptionPane.showMessageDialog(null, "invalid input", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
         int i = 0;
         for(Professor.registercours r1: regcoursehistory.getRegcoursehistory()){
             if(r1.getNEUID().equals(neuid)){
               r1.setStautofjob("Rejected");
               JOptionPane.showMessageDialog(null, "Rejected!");
               i++;
               break;
             }
         }
         if(i==0){
              JOptionPane.showMessageDialog(null, "Application Expired or invalid request");
         }
                 
        
