    private void AcceptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AcceptBtnActionPerformed
        // TODO add your handling code here:
        String stname = txtStudentName.getText();
        String neuid = txtneuid.getText();
        String Role = txtRole.getText();
        String Status = txtstatus.getText();
         Employer.EmployerJobRoles emproles = eh.SearchEmployerJobRolesbyid(empid);
        if(stname.equals("")){
            JOptionPane.showMessageDialog(null, "invalid input", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
         if(neuid.equals("")){
            JOptionPane.showMessageDialog(null, "invalid input", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
         if(Role.equals("")){
            JOptionPane.showMessageDialog(null, "invalid input", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
         if(Status.equals("")){
            JOptionPane.showMessageDialog(null, "invalid input", "Warning", JOptionPane.WARNING_MESSAGE); 
        }
         int i = 0;
         for(Professor.registercours r1: regcoursehistory.getRegcoursehistory()){
             if(r1.getNEUID().equals(neuid)){
               r1.setStautofjob("Hired");
               JOptionPane.showMessageDialog(null, "Hired");
               i++;
               for(JobRoles j: emproles.getEmp_rolelists()){
                   
                   if(j.getRole_name().equals(Role)){
                      j.setRoleavailability(j.getRoleavailability()-1); 
                       break;
                   }
                   
              break;
               }
               
             }
         }
         if(i==0){
              JOptionPane.showMessageDialog(null, "Application Expired or invalid request");
         }
                 
    }//GEN-LAST:event_AcceptBtnActionPerformed

